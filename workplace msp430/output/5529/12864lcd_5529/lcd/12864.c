//读数据子程序，串行接口不可用
unsigned char reddat_12864(void)
{
 unsigned char i,temp1,temp2;
 temp1=temp2=0;
 for(i=0;i<8;i++)
 {
 temp1=temp1<<1;
 SCLK_0;
 SCLK_1;
 SCLK_0;
 if(P1IN&&0x40==1) temp1++;
 }
 for(i=0;i<8;i++)
 {
 temp2=temp2<<1;
 SCLK_0;
 SCLK_1;;
 SCLK_0;
 if(P1IN&&0x40==1) temp2++;
 }
 return ((0xf0&temp1)+(0x0f&temp2));
}

//画点函数 ,左上角为参考点（ 0，0）
//右下角为（ 63,127），点坐标形式为 (行坐标，列坐标 )
//参数 type 用于设置画黑点、白点或取反（黑变白，白变黑）
//type = 0 为白色， 1 为黑色， 2 为取反,3为所属16个位置全部亮
void drawdot_12864(unsigned char y,unsigned char x,unsigned char type)
{
 unsigned char X,Y,k; //X 存储行地址， Y 存储列地址
 //k 存储点在字中的位置从左至右为 0~15
 unsigned char DH,DL; // 存放读出数据的高字节和低字节
 int number;
 if(y < 32){ // 算法：确定所画点的地址行与列地址
 X = 0x80 + (x >> 4);
 Y = 0x80 + y;

 }
 else{
 X = 0x88 + (x >> 4);
 Y = 0x80 + (y - 32);
 }
 LCD_Write_cmd(0x34); // 开启扩展指令，关闭绘图显示
 number=(8*y+x/16)*2;
// LCD_Write_cmd(Y); // 写入所确定的点的行位地址
// LCD_Write_cmd(X); //写入所确定的点的列字地址
// DH = reddat_12864(); // 假读
// DH = reddat_12864(); // 读高字节
// DL = reddat_12864(); // 读低字节
 DH=memory[number];
 DL=memory[number+1];

 k = x % 16; // 余数为点在字中的位置
 //画点
 switch(type){ // 画点类型， 1 黑或 0 白或 2 取反 3 全凉
 case 0:
 if(k < 8){ // 点在高字节
 DH &= ~(0x01 << (7 - k)); // 修改该点同时保持其他位不变
 memory[number]=DH;
 memory[number+1]=DL;
 }else{ // 点在低字节
 DL &= ~(0x01 << (7 - (k % 8))); // 修改该点同时保持其他位不变
 memory[number]=DH;//存到内存数组中
 memory[number+1]=DL;//存到内存数组中
 }
 break;
 case 1:
 if(k < 8){

 DH |= (0x01 << (7 - k)); // 修改该点同时保持其他位不变
 memory[number]=DH;
 memory[number+1]=DL;
 }else{
 DL |= (0x01 << (7 - (k % 8))); // 修改该点同时保持其他位不变
 memory[number]=DH;
 memory[number+1]=DL;
 }
 break;
 case 2:
 if(k < 8){
 DH ^= (0x01 << (7 - k)); // 修改该点同时保持其他位不变
 memory[number]=DH;
 memory[number+1]=DL;
 }else{
 DL ^= (0x01 << (7 - (k % 8))); // 修改该点同时保持其他位不变
 memory[number]=DH;
 memory[number+1]=DL;
 }
 break;
 case 3:
     DH=0xff;
     DL=0xff;
     memory[number]=DH;
     memory[number+1]=DL;
     break;
 default:
 break;
 }
 LCD_Write_cmd(Y); // 写行位地址
 LCD_Write_cmd(X); // 写列字地址
 LCD_Write_Byte(DH); // 将高字节数据写回
 LCD_Write_Byte(DL); // 将低字节数据写回
 LCD_Write_cmd(0x30); // 转回普通指令




}
