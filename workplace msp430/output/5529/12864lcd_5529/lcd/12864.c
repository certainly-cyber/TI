//x取0-15        y取0-63
void LCD_ShowStrCH(char x, char y, char* ch, char num)
{
    LCD_Write_cmd(0x34);        //打开扩展指令集
    while (num--)
    {
        LCD_ShowCharCH(x++, y, ch);
        ch += 32;
    }
    LCD_Write_cmd(0x36);               //打开绘图显示
    LCD_Write_cmd(0x30);               //回到基本指令集
}
//x取0-15(上）16-31（下）        y取0-63s
void LCD_ShowStr(char x, char y, char* ch)
{
    LCD_Write_cmd(0x34);        //打开扩展指令集
    char i = 0;
    for (;;)
    {
        i++;
        if (*ch++ == '\0')
        {
            if (i == 2)
            {
                LCD_ShowChar(x++, y, *(ch - 2), ' ');
            }
            break;
        }
        else if (i == 2)
        {
            i = 0;
            LCD_ShowChar(x++, y, *(ch - 2), *(ch - 1));
        }
    }

    LCD_Write_cmd(0x36);               //打开绘图显示
    LCD_Write_cmd(0x30);               //回到基本指令集
}

//把两个16X8的模合成一个16X16的模
void Str2IN1(char a1, char a2)
{
    char i;
    unsigned int c1, c2;
    c1 = (a1 - 32) * 16;
    c2 = (a2 - 32) * 16;

    for (i = 0; i < 16; i++)
    {
        ONE_CHAR[i * 2] = F8X16[c1++];
        ONE_CHAR[i * 2 + 1] = F8X16[c2++];
    }
}

void LCD_ClearPic(void)
{
    uchar i, x, y;


    LCD_Write_cmd(0x34);        //打开扩展指令集
    for (i = 0; i < 2; i++)   //上下屏写入
    {
        for (y = 0; y < 32; y++)   //垂直Y写32次
        {
            LCD_Write_cmd(0x80 + y);       //行地址
            LCD_Write_cmd(0x80 + i * 8);   //列地址

            for (x = 0; x < 16; x++)   //横向X写8次
            {
                LCD_Write_Byte(0x00);     //写字节数据
            }
        }
    }

    LCD_Write_cmd(0x36);               //打开绘图显示
    LCD_Write_cmd(0x30);               //回到基本指令集
}
